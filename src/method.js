export default function dayOfWeek (string) {
  if (string === 1) {
    return "monday";
  }
  else if (string === 2) {
    return "tuesday";
  } 
  else if (string === 3) {
    return "wednesday";
  }
  else if (string === 4) {
    return "thursday";
  }
  else if (string === 5) {
    return "friday";
  }
  else if (string === 6) {
    return "saturday";
  }
  else if (string === 7) {
    return "sunday";
  }
}
